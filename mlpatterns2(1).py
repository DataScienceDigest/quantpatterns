def add_technical_indicators(df):
    df["body_pct"] = ((df["close"] - df["open"]) / df["open"]) * 100 # Candle Body %
    df["range_pct"] = ((df["high"] - df["low"]) / df["open"]) * 100 # Candle Range %
    df["upper_wick_pct"] = ((df["high"] - np.maximum(df["open"], df["close"]))/ df["open"]) * 100 # Upper Wick %
    df["lower_wick_pct"] = ((np.minimum(df["open"], df["close"]) - df["low"])/ df["open"]) * 100 # Lower Wick %
    df["candle_direction"] = np.where(df["close"] > df["open"],1,np.where(df["close"] < df["open"], -1, 0)) # Candle Direction1 = Bullish -1 = Bearish  0 = Neutral
    df["hlc3"] = (df["high"] +df["low"] +df["close"]) / 3 # HLC3 = (High + Low + Close) / 3
    df["ohlc4"] = (df["open"] +df["high"] +df["low"] +df["close"]) / 4 # OHLC4 = (Open + High + Low + Close) / 4
    df["gap_pct"] = ((df["open"] - df["close"].shift(1))/ df["close"].shift(1)) * 100 # Gap % Current Open vs Previous Close
    df['ema_9'] = df.ta.ema(9)
    df['ema_20'] = df.ta.ema(20)
    df['ema_50'] = df.ta.ema(50)
    df["close_ema9_dist"] = ((df["close"] - df["ema_9"]) / df["ema_9"]) * 100 # Distance of Close from EMA 9
    df["close_ema20_dist"] = ((df["close"] - df["ema_20"]) / df["ema_20"]) * 100 # Distance of Close from EMA 20
    df["close_ema50_dist"] = ((df["close"] - df["ema_50"]) / df["ema_50"]) * 100 # Distance of Close from EMA 50
    df["ema9_above_20"] = (df["ema_9"] > df["ema_20"]).astype(int) # EMA 9 above EMA 20
    df["ema20_above_50"] = (df["ema_20"] > df["ema_50"]).astype(int) # EMA 20 above EMA 50
    df['rsi_14'] = df.ta.rsi(length=14)
    macd = df.ta.macd(fast=12, slow=26, signal=9)
    df['macd'] = macd['MACD_12_26_9']
    df['macd_hist'] = macd['MACDh_12_26_9']
    df['macd_signal'] = macd['MACDs_12_26_9']
    df['roc_10'] = df.ta.roc(length=10)
    stoch = df.ta.stoch(fast_k=14, slow_k=3, slow_d=3)
    df['stoch_k'] = stoch.iloc[:, 0]
    df['stoch_d'] = stoch.iloc[:, 1]
    df['volume_sma_20'] = df['volume'].rolling(20).mean() # Volume
    df['rvol'] = df['volume'] / df['volume'].rolling(14).mean() # Manual RVOL
    df['volume_spike'] = (df['volume'] > df['volume'].rolling(10).mean() * 1.5).astype(int) # Best balance for most traders
    df['obv'] = df.ta.obv()
    df['cmf'] = df.ta.cmf(length=14)
    df['atr_14'] = df.ta.atr(length=14) # Volatility
    bb = df.ta.bbands(length=20)
    df['bb_lower'] = bb['BBL_20_2.0']
    df['bb_middle'] = bb['BBM_20_2.0']
    df['bb_upper'] = bb['BBU_20_2.0']
    df['bb_width'] = bb['BBB_20_2.0'] # Bollinger Band Width
    df['bb_percent'] = bb['BBP_20_2.0'] # Bollinger Band Position
    # df['vwap'] = df.ta.vwap() # VWA
    # df['close_vwap_dist'] = ((df['close'] - df['vwap']) / df['vwap']) * 100 # Distance from VWAP
    df['rolling_high_20'] = (df['high'].rolling(20).max()) # Previous 20 candle highest high
    df['rolling_low_20'] = (df['low'].rolling(20).min()) # Previous 20 candle lowest low
    df['prev_high_break'] = (df['close'] >df['rolling_high_20'].shift(1)).astype(int) # Current close breaks previous rolling high
    df['prev_low_break'] = (df['close'] <df['rolling_low_20'].shift(1)).astype(int) # Current close breaks previous rolling low
    df['inside_bar'] = ((df['high'] < df['high'].shift(1)) &(df['low'] > df['low'].shift(1))).astype(int)  # Inside Bar Current candle inside previous candle range
    df['outside_bar'] = ((df['high'] > df['high'].shift(1)) &(df['low'] < df['low'].shift(1))).astype(int) # Outside Bar  Current candle engulfs previous candle range
    green = (df['close'] > df['open']).astype(int) # Green Candle
    red = (df['close'] < df['open']).astype(int) # Red Candle
    df['consecutive_green'] = (green.groupby((green != green.shift()).cumsum()).cumsum()) # Consecutive Green Candles
    df['consecutive_red'] = (red.groupby((red != red.shift()).cumsum()).cumsum()) # Consecutive Red Candles
    future_period = 5

    df['future_max_high'] = (
        df['high']
        .rolling(future_period)
        .max()
        .shift(-future_period)
    )
    df['future_max_return'] = (
    (df['future_max_high'] - df['close'])
    / df['close']
    ) * 100

    df['atr_pct'] = (
        df['atr_14'] / df['close']
    ) * 100
    df['future_close'] = (df['close'].shift(-future_period))
    df['future_return'] = ((df['future_close'] - df['close'])/ df['close']) * 100
    df['future_min_low'] = (df['low'].rolling(future_period).min().shift(-future_period))
    df['future_min_return'] = ((df['future_min_low'] - df['close'])/ df['close']) * 100
    atr_multiplier = 0.5
    df['dynamic_threshold'] = (df['atr_pct']* atr_multiplier)
    df['target'] = (df['future_return']> df['dynamic_threshold']).astype(int)
    # patterns = df.ta.cdl_pattern(name="all")
    # df = pd.concat([df, patterns], axis=1)
    df.dropna(inplace=True)
    df['target'] = (df['future_max_return'] > (df['atr_pct'] * 0.75)).astype(int)

    return df

