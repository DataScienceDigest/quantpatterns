# ============================================================
# TRADE SIMULATION
# ============================================================

holding_period = 5

trade_results = []

for idx in results.index:

    row = results.loc[idx]

    # Only signals
    if row['signal'] != 1:
        continue

    entry = row['entry_price']
    sl = row['stoploss']
    tp = row['target_price']


    # Get the position of idx
    pos = results.index.get_loc(idx)

    # Use iloc with position-based slicing
    future_data = results.iloc[pos : pos + holding_period]

    trade_exit = None

    for _, candle in future_data.iterrows():

        high = candle['high']
        low = candle['low']

        # Stoploss hit
        if low <= sl:
            trade_exit = sl
            break

        # Target hit
        elif high >= tp:
            trade_exit = tp
            break

    # Timeout exit
    if trade_exit is None:
        trade_exit = future_data.iloc[-1]['close']

    # Trade Return
    trade_return = ((trade_exit - entry)/ entry) * 100

    trade_results.append({
        'entry': entry,
        'exit': trade_exit,
        'return_pct': trade_return
    })


